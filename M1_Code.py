cubeEdge = input('Please enter the length of any edge of a cube: \n')

# Calculation of surface area for one of the sides of a cube
areaSquare = int(cubeEdge) ** 2

# Calculation for surface area of a cube
areaCube = int(cubeEdge) ** 2 * 6

# Calculation for volume of a cube
volumeCube = int(cubeEdge) ** 3

print('Surface area on one side of cube:', areaSquare)
print('Surface area of cube:', areaCube)
print('Volume of cube:', volumeCube)
